#!/usr/bin/env python3
"""
Batch 4: Generate displaced-control pairings + blinding key.

Inputs:
    events/event_registry_batch4.json  (60 events with event_id, title, etc.)
    pairing_seed_batch4.txt            (seed for X->Y derangement)
    blinding_seed_batch4.txt           (seed for A/B randomization)

Outputs:
    pairings_batch4.json               (X -> Y mapping for displaced controls)
    ../interpretations/blinding_key_batch4.json  (SEALED until scoring done)
"""
import json
import os
import random
import sys

HERE = os.path.dirname(os.path.abspath(__file__))
REGISTRY_PATH = os.path.join(HERE, "..", "events", "event_registry_batch4.json")
PAIRING_SEED_PATH = os.path.join(HERE, "pairing_seed_batch4.txt")
BLINDING_SEED_PATH = os.path.join(HERE, "blinding_seed_batch4.txt")
PAIRINGS_OUT = os.path.join(HERE, "pairings_batch4.json")
BLINDING_OUT = os.path.join(HERE, "..", "interpretations", "blinding_key_batch4.json")


def load_seed(path: str) -> int:
    with open(path) as f:
        return int(f.read().strip())


def derangement(event_ids: list[str], seed: int) -> dict[str, str]:
    """Return a permutation event_id -> event_id with no fixed points.

    Uses Sattolo's algorithm to guarantee a derangement that is also a single
    cycle (every event is both a control source for exactly one other event
    AND has exactly one event as its control source — uniform usage)."""
    rng = random.Random(seed)
    items = event_ids[:]
    rng.shuffle(items)
    n = len(items)
    perm = items[:]
    for i in range(n - 1, 0, -1):
        j = rng.randrange(i)  # strictly < i, so no fixed points
        perm[i], perm[j] = perm[j], perm[i]
    return dict(zip(items, perm))


def blinding_assignment(event_ids: list[str], seed: int) -> list[dict]:
    """Randomize A/B label per event."""
    rng = random.Random(seed)
    out = []
    for eid in event_ids:
        real_is_A = rng.choice([True, False])
        out.append(
            {
                "event_id": eid,
                "real_is_A": real_is_A,
                "label_A": "real" if real_is_A else "control",
                "label_B": "control" if real_is_A else "real",
            }
        )
    return out


def main():
    with open(REGISTRY_PATH) as f:
        registry = json.load(f)
    event_ids = [e["event_id"] for e in registry["events"]]
    if len(event_ids) != registry.get("total_events", len(event_ids)):
        print(f"WARNING: registry total_events mismatch", file=sys.stderr)
    if len(set(event_ids)) != len(event_ids):
        sys.exit("ERROR: duplicate event_ids in registry")

    pairing_seed = load_seed(PAIRING_SEED_PATH)
    blinding_seed = load_seed(BLINDING_SEED_PATH)

    pairings = derangement(event_ids, pairing_seed)
    assert all(x != y for x, y in pairings.items()), "fixed point in pairings"
    assert set(pairings.keys()) == set(pairings.values()), "non-bijection"
    assert len(set(pairings.values())) == len(pairings), "duplicate control source"

    blinding = blinding_assignment(event_ids, blinding_seed)

    pairings_doc = {
        "generated": "2026-05-07",
        "batch": 4,
        "method": "displaced 取象法 controls — control hexagram for event X is generated by running 取象法 on event Y where Y = pairing[X]",
        "pairing_seed": pairing_seed,
        "guarantees": [
            "no event paired with itself (X != Y)",
            "each event Y is the control source for exactly one event X",
            "pairing is a single cycle (Sattolo's algorithm)",
        ],
        "pairings": [
            {"event_id": x, "control_source": y} for x, y in pairings.items()
        ],
    }
    with open(PAIRINGS_OUT, "w") as f:
        json.dump(pairings_doc, f, ensure_ascii=False, indent=2)
    print(f"Wrote {PAIRINGS_OUT}")

    blinding_doc = {
        "generated": "2026-05-07",
        "batch": 4,
        "warning": "SEALED until ALL scoring is complete. Do not open during interpretation, narration, or scoring phases.",
        "blinding_seed": blinding_seed,
        "events": blinding,
    }
    with open(BLINDING_OUT, "w") as f:
        json.dump(blinding_doc, f, ensure_ascii=False, indent=2)
    print(f"Wrote {BLINDING_OUT}")

    a_count = sum(1 for e in blinding if e["real_is_A"])
    print(f"Blinding sanity: real_is_A in {a_count}/{len(blinding)} events ({a_count/len(blinding):.0%})")


if __name__ == "__main__":
    main()
